		AREA mycode,CODE,READONLY
		ENTRY
		EXPORT start
			
start PROC

        LDR R0,=0xFFFFFFFF     ;LOWER BITS OF FIRST NUMBER
		LDR R1,=0x00000001      ;UPPER BITS OF FIRST NUMBER
		
		LDR R2,=0x00000001      ;LOWER BITS OF SECOND NUMBER
		LDR R3,=0x00000000      ;UPPER BITS OF SECOND NUMBER
		
	  	
		SUBS R4,R0,R2
		SBC R5,R1,R3
		
		B .
		ENDP
		END